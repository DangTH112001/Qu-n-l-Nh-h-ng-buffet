    private void btn_ConfirmMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_ConfirmMouseClicked
        DateFormat format = new SimpleDateFormat("dd/MM/yyyy");
        String HoTen = tf_FullName.getText();
        String GT = (String) cb_Gender.getSelectedItem();
        String SN = format.format(dc_Birthday.getDate());
        String DiaChi = tf_Address.getText();
        String SDT = tf_Phone.getText();
          
        String query = "UPDATE KHACHHANG "
                + "SET HOTEN = ?, GIOITINH = ?, NGSINH = ?, DIACHI = ?, SDT = ?"
                + "WHERE MAKH = ?";
        
        try {
            PreparedStatement stm = SQLTable.connection.prepareStatement(query);
            stm.setString(1, HoTen);
            stm.setString(2, GT);
            stm.setDate(3, new java.sql.Date(dc_Birthday.getDate().getTime()));
            stm.setString(4, DiaChi);
            stm.setString(5, SDT);
            stm.setString(6, CustomerHome.MaKH);
            
            stm.execute();
            JOptionPane.showMessageDialog(null, "Update success");
            data = new Object[] {HoTen, GT, SN, DiaChi, SDT, data[5]};
            dispose();
        }
        catch (Exception ex) {
            System.out.println(ex.getMessage());
            lb_Error.setVisible(true);
            setBorder();
        }
    }//GEN-LAST:event_btn_ConfirmMouseClicked
    
