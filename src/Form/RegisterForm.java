    private void btn_ConfirmMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_ConfirmMouseClicked
        DateFormat format = new SimpleDateFormat("dd/MM/yyyy");
        
        if (isOK()) {
            String FullName = tf_FullName.getText();
            String Gender = (String) cb_Gender.getSelectedItem();
            String Type = "0";
            String Phone = tf_Phone.getText();
            String Address = tf_Address.getText();
            String Birth = format.format(dc_Birthday.getDate());
            String Reg = format.format(new Date());
            String Account  = tf_UserName.getText();
            String Password = tf_RetypePassword.getText();
            String InviteCode = tf_InviteCode.getText();
            String Point = "0";
            String ID = SQLTable.getTableID("KHACHHANG", "KH");
            
            Object[] data = {ID, FullName, Gender, Birth, Type, Phone, Address, Reg, Account, Password, InviteCode, Point};
            KhachHangController.add(data);
            LoginForm form = new LoginForm();
            form.setVisible(true);
            dispose();   
        }
        else {
            lb_Error.setVisible(true);
            setBorder();
        }
    }//GEN-LAST:event_btn_ConfirmMouseClicked
    
    private void setBorder() {
        Border errorBorder = BorderFactory.createLineBorder(Color.RED, 1);
        tf_UserName.setBorder(errorBorder);
        tf_RetypePassword.setBorder(errorBorder);
        tf_Password.setBorder(errorBorder);
        tf_Phone.setBorder(errorBorder);
        tf_Address.setBorder(errorBorder);
        tf_FullName.setBorder(errorBorder);
        dc_Birthday.setBorder(errorBorder); 
    }
    
    private boolean isOK() {
        return !isEmpty() && isMatch();
    }
    
    private boolean isEmpty() {
        return tf_UserName.getText().isEmpty() && tf_RetypePassword.getText().isEmpty() 
            && tf_Password.getText().isEmpty() && tf_FullName.getText().isEmpty()
            && tf_Phone.getText().isEmpty() && tf_Address.getText().isEmpty()
            && (dc_Birthday.getDate() == null);
    }
    
    
    private boolean isMatch() {
        return tf_RetypePassword.getText().equals(tf_Password.getText());
    }
